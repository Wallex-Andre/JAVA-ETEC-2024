package com.mycompany.quantidadeentre6000e12000;

public class QuantidadeEntre6000e12000 {
    public static void main(String[] args) {
        int inicio = 6000;
        int fim = 12000;
        int quantidade = fim - inicio + 1;
        System.out.println("A quantidade de números entre 6000 e 12000 é: " + quantidade);
    }
}